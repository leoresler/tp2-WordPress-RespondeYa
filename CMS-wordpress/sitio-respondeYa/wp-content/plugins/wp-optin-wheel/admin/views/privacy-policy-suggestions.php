<h3>Cookies</h3>
<p>
	WP Optin Wheel uses cookies to prevent your visitors from seeing the wheel until they are allowed to see it again
	(depending on your wheel settings).
</p>
<p>The following cookie is set by the plugin:</p>
<ul>
	<li><b>Name: </b>wof-XX, where XX is the ID of your wheel. Example: wof-129.</li>
	<li><b>Value: </b>1.</li>
	<li><b>Expiry: </b> this depends on the wheel's settings.</li>
	<li><b>Use of the cookie:</b> to help determine whether or not the user is allowed to see the wheel on the frontend.</li>
</ul>

<h3>Storage</h3>
<p>
	In addition to using cookies, we may also use the browser's storage capabilities (sessionStorage or localStorage) to prevent people from seeing the popup when they shouldn't.
</p>

<h3>Data the Plugin Captures</h3>
<p>
	If you have turned on 'logging' in the settings, WP Optin Wheel captures data to a log file. Here's a list of what is captured:
</p>
<ul>
	<li>- The player's email address.</li>
	<li>- The wheel ID.</li>
	<li>- A timestamp of when this log entry was inserted.</li>
	<li>- Whether or not the game was won, and the associated prize.</li>
</ul>

<p>We also store hashed email addresses in your WordPress database to prevent users from playing multiple times with the same address.</p>

<h3>Where We Send the Data</h3>
<p>
	WP Optin Wheel is meant to be connected to a 3rd party tool (as defined in the 'Email Integration' tab)
	such as Mailchimp. We send the user's data (email address) to that tool via their API.
	The 3rd party  will store this data until the user is unsubscribed/deleted.
</p>